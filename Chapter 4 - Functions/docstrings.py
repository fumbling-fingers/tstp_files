# When you write a function, it is good practice to leave a comment
# called a docstring at the top of the function explaining what data type each parameter needs to be. Docstrings explain what the function does, and document what kinds of parameters it needs:


def add(x, y):
    """
    Returns x + y.
    :param x: int.
    :param y: int.
    :return: int sum of x and y.
    """
    return x + y