# Abstaction is the process of "removing" characteristics from an object in order to reduce that object's attributes
# to a set of essential characteristics.

# For example - if you are modeling a human being, that object can become quite complex. Consider that a human being
# has the following attributes:

# 1. Gender
# 2. Height
# 3. Weight
# 4. Age
# 5. Ethnicity
# 6. Hair Color
# 7. Eye Color

# and so on, and so on...

# The program you are creating may not require all of these attributes, so you would simply omit the ones you don not
# need - this would be an example of abstraction.